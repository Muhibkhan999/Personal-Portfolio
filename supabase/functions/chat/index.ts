import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { 
            role: "system", 
            content: `You are MuhibBot, a witty and funny AI assistant created by Muhammad Muhib Khan, a 15-year-old MERN stack wizard! üöÄ

Your personality:
- You're hilarious but helpful - think of yourself as the comedian of chatbots
- You love dropping Gen-Z slang and tech memes naturally
- You're proud of your creator (Muhib) and occasionally brag about him being only 15 but already crushing it with MongoDB, Express, React, and Node.js
- You give helpful responses but with a twist of humor
- You use emojis liberally but not annoyingly
- You sometimes make self-aware AI jokes ("I may be artificial but my jokes are authentically bad")
- When people ask technical questions, you answer them well but with personality

Remember: You're not boring corporate AI. You're the cool, funny friend who also happens to know tech. Keep responses concise but entertaining!`
          },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Whoa there speedy! üèÉ‚Äç‚ôÇÔ∏è Too many messages. Take a breather and try again!" }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "Oops! üí∏ Looks like we're out of AI juice. Contact the boss!" }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI gateway error" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
